def main():
    with open(DATA_FILE, 'r', encoding='utf-8') as f:
        vocab = json.load(f)
        
    with open(MISSING_ENTRIES_FILE, 'r', encoding='utf-8') as f:
        missing_entries = json.load(f)

    # Patch simple sentences
    for entry in vocab:
        if not entry.get('example_sentence_es'):
            en = entry.get('example_sentence_en', '')
            if en in TRANSLATIONS:
                entry['example_sentence_es'] = TRANSLATIONS[en]
            else:
                entry['example_sentence_es'] = "[PENDIENTE] " + en
                
    # Function to create full entry
    def make_entry(row, idx):
        eng = row['english']
        # The two missing are wake up / get up from block 6
        # BLOCK 6: category='daily-life', difficulty='easy'
        ej_es = TRANSLATIONS.get(row['example_en'], "[PENDIENTE] " + row['example_en'])
        
        return {
            "id": f"voc-{idx:04d}",
            "english_word": eng,
            "spanish_translation": row['spanish'],
            "example_sentence_en": row['example_en'],
            "example_sentence_es": ej_es,
            "category": "daily-life",
            "difficulty": "easy",
            "tags": ["daily-life", "routine", "actions", "multi-word"],
            "note": "También aparece en el bloque de phrasal verbs"
        }

    # Since we need 486 entries and there are 484, we just append them to the end
    # re-assigning IDs from 1 to 486 might be better.
    # Let's insert them and then reassign IDs based on alphabetical or original order?
    # Original order is preserved in vocab list. The missing ones were in block 6.
    
    # Actually, to keep it simple, just append and re-sort by id is NOT correct if id was sequential!
    # Let's just append them to the list and then rebuild IDs purely sequential 1..486.
    
    # Where to insert? Let's just append and rebuild sequential ids for all, 
    # to maintain exactly 486 sequential ids without gaps
    
    # Wait, it's better to insert them where block 6 is approx.
    # We find the first 'shopping' or 'food' entry and insert before.
    insert_idx = 0
    for i, e in enumerate(vocab):
        if e['category'] == 'food':
            insert_idx = i
            break
            
    if insert_idx == 0: insert_idx = len(vocab)
    
    for row in reversed(missing_entries):
        vocab.insert(insert_idx, make_entry(row, 0)) # temp id
        
    # Reassign IDs completely sequentially to fix gaps
    for i, entry in enumerate(vocab):
        entry['id'] = f"voc-{i+1:04d}"

    # Verify no missing example_sentence_es
    missing_count = sum(1 for e in vocab if not e.get('example_sentence_es') or e.get('example_sentence_es').startswith("[PENDIENTE]"))
    
    with open(OUT_FILE, 'w', encoding='utf-8') as f:
        json.dump(vocab, f, ensure_ascii=False, indent=2)

    print(f"Total entries now: {len(vocab)}")
    print(f"Empty or pending translations: {missing_count}")
    print("[OK] Finished patching")

if __name__ == '__main__':
    main()
